// Comparison Operators

// Comparison operators - the evaluators
let score  = 50;
// greater than or equal to
let passing = score >= 70;     // true
console.log(passing);
// strict equality
let perfect = score === 100;   // false
console.log(perfect);
// loose equality (don't use this often)
let perfectString = score == "100";
console.log(perfectString)



// Logical operators (boolean logic operators)

// check that both of these things are true
// &&

console.log(true && false);

// one of these is true
// ||
console.log(false || false || false || false);

// invert the value
// !
// let isNotTuesday = true; dont do this

